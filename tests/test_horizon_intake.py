    def test_source_workflow_fields_explain_pending_and_monitoring_records(self) -> None:
        workflow_fields = {
            "Retention Rationale",
            "Pending Reason",
            "Next Action",
            "Blocker",
            "Monitoring Rationale",
            "Monitoring Group",
            "Monitoring Baseline",
        }
        for fields in self.source_catalog_fields.values():
            self.assertTrue(workflow_fields <= fields)
        for row in self.pending_sources:
            for field in ("Retention Rationale", "Pending Reason", "Next Action"):
                self.assertTrue(row[field].strip(), f"{row['Source ID']} lacks {field}")
        for row in self.sources:
            if row["Monitoring"] == "Yes":
                rationale = row["Monitoring Rationale"].strip()
                self.assertGreaterEqual(len(rationale), 60, row["Source ID"])
                self.assertNotEqual(
                    rationale,
                    "Monitor the live docket for a proposition-bearing disposition.",
                    row["Source ID"],
                )
                self.assertIsNone(
                    re.fullmatch(r"Monitor\s+[A-Z]\.", rationale),
                    row["Source ID"],
                )
                self.assertTrue(row["Monitoring Group"].strip(), row["Source ID"])

    def test_open_tracker_case_records_are_marked_for_monitoring(self) -> None:
        for rows in self.source_catalogs.values():
            for row in rows:
                if row["Source Type"] != "Court Docket or Judicial Record":
                    continue
                match = re.search(
                    r"tracker status:\s*([^.;]+(?:\([^)]*\))?)",
                    row["Proposition Supported"],
                    flags=re.IGNORECASE,
                )
                if not match or match.group(1).casefold().startswith("case closed"):
                    continue
                self.assertEqual(row["Monitoring"], "Yes", row["Source ID"])

